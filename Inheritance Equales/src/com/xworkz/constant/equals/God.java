package com.xworkz.constant.equals;

public class God {
	
	String name;
	String gender;
	float weight;
	int height;
	boolean divinPower;
	String vehicle;
	String wifeName;
	String sonName;
	String place;
	int noOfChildern;
	
	
	
	public God(String name, String gender, float weight, int height, boolean divinPower, String vehicle,
			String wifeName, String sonName, String place, int noOfChildern) {
		super();
		this.name = name;
		this.gender = gender;
		this.weight = weight;
		this.height = height;
		this.divinPower = divinPower;
		this.vehicle = vehicle;
		this.wifeName = wifeName;
		this.sonName = sonName;
		this.place = place;
		this.noOfChildern = noOfChildern;
	}
	public String getName() {
		return name;
	}
	public String getGender() {
		return gender;
	}
	public float getWeight() {
		return weight;
	}
	public int getHeight() {
		return height;
	}
	public boolean isDivinPower() {
		return divinPower;
	}
	public String getVehicle() {
		return vehicle;
	}
	public String getWifeName() {
		return wifeName;
	}
	public String getSonName() {
		return sonName;
	}
	public String getPlace() {
		return place;
	}
	public int getNoOfChildern() {
		return noOfChildern;
	}
	public void setName(String name) {
		this.name = name;
	}
	public void setGender(String gender) {
		this.gender = gender;
	}
	public void setWeight(float weight) {
		this.weight = weight;
	}
	public void setHeight(int height) {
		this.height = height;
	}
	public void setDivinPower(boolean divinPower) {
		this.divinPower = divinPower;
	}
	public void setVehicle(String vehicle) {
		this.vehicle = vehicle;
	}
	public void setWifeName(String wifeName) {
		this.wifeName = wifeName;
	}
	public void setSonName(String sonName) {
		this.sonName = sonName;
	}
	public void setPlace(String place) {
		this.place = place;
	}
	public void setNoOfChildern(int noOfChildern) {
		this.noOfChildern = noOfChildern;
	}
	

}
